<?php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Expose
 */
class Expose extends Document
{
    /**
     * @var string
     */
    private $matiere;

    /**
     * @var string
     */
    private $typeExpose;

    /**
     * Set matiere
     *
     * @param string $matiere
     * @return Expose
     */
    public function setMatiere($matiere)
    {
        $this->matiere = $matiere;

        return $this;
    }

    /**
     * Get matiere
     *
     * @return string 
     */
    public function getMatiere()
    {
        return $this->matiere;
    }

    /**
     * Set typeExpose
     *
     * @param string $typeExpose
     * @return Expose
     */
    public function setTypeExpose($typeExpose)
    {
        $this->typeExpose = $typeExpose;

        return $this;
    }

    /**
     * Get typeExpose
     *
     * @return string 
     */
    public function getTypeExpose()
    {
        return $this->typeExpose;
    }
}
