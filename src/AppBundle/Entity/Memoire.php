<?php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Memoire
 */
class Memoire extends Document
{
    /**
     * @var string
     */
    private $domaine;

    /**
     * Set domaine
     *
     * @param string $domaine
     * @return Memoire
     */
    public function setDomaine($domaine)
    {
        $this->domaine = $domaine;

        return $this;
    }

    /**
     * Get domaine
     *
     * @return string 
     */
    public function getDomaine()
    {
        return $this->domaine;
    }
}
